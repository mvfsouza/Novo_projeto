#Tipos Int e Float
#int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal e considerado
# positivo
print(11)
print(-11)
print(0)

#Float -> Número com ponto Flutuante
# O tipo Float representa qualquer número
# posito ou negativo com ponto flutuante
# float sem sinal é considerado positivo
print(1.1, 10.11)
print(10.10, -10.50)
# A função type mostra o tipo que e python
# inferiu ao valor

print(type('Marcos Vinicius'))
print(type(9))
print(type(10), type('marcos vincius'), type(0.0))


